package day17_whileLoop;

public class WhileLOOP {
    public static void main(String[] args) {
       // if(10>9){System.out.println("HELLO");}
     //   while (10>9){System.out.println("hello");}//it writes infinitive times
        for(int i=0;i<=5;i++){
          // initali;cond;iterator
            System.out.println(i);
        }
        int i=0;
        while(i<=5){
          //i++; put here result=0,1,2,3,4,5,6 because of top to bottom
            System.out.println(i);
            i++;
        }











    }
}
